#!/usr/bin/env python3
"""
run_and_evaluate.py — run scnoisemeter on the synthetic BAM and score its output
against the ground-truth labels generated by synthesize_bam.py.

Two outputs:

  Exp 1 — confusion matrix.
          Each Exp 1 CB holds reads of a single true category. We read
          scnoisemeter's cell_metrics.tsv and convert per-category fractions
          back into counts. The result is a (true × assigned) confusion matrix
          plus per-category precision / recall / F1.

  Exp 2 — noise-fraction dose-response.
          Each Exp 2 CB holds a known mixture of signal + noise. We compare
          the reported noise_read_frac to the true noise fraction encoded in
          the CB name.

Usage:
  python3 run_and_evaluate.py \\
      --bam      /tmp/scnm_bench/synthetic.bam \\
      --truth    /tmp/scnm_bench/ground_truth.tsv \\
      --gtf      ~/.cache/scnoisemeter/gencode.v49.annotation.gtf.gz \\
      --fasta    /data/scNoiseMeter/GRCh38.primary_assembly.genome.fa.gz \\
      --repeats  /tmp/scnm_bench/repeats.bed \\
      --outdir   /tmp/scnm_bench/out \\
      [--skip-run]     # reuse an existing scnoisemeter output directory

--repeats is the RepeatMasker BED emitted by synthesize_bam.py. Passing it
lets scnoisemeter classify INTERGENIC_REPEAT reads; omit it to test the
HOTSPOT/NOVEL path without REPEAT.
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from collections import defaultdict
from pathlib import Path

import pandas as pd


CATEGORY_COLUMNS = [
    "exonic_sense", "exonic_antisense",
    "intronic_jxnspan", "intronic_pure", "intronic_boundary",
    "intergenic_sparse", "intergenic_repeat", "intergenic_hotspot", "intergenic_novel",
    "chimeric", "mitochondrial", "multimapper",
    "ambiguous", "ambiguous_cod_ncod", "ambiguous_cod_cod",
    "unassigned",
]

CONSERVATIVE_NOISE = {
    "exonic_antisense", "intronic_pure", "intronic_boundary",
    "intergenic_sparse", "intergenic_repeat", "intergenic_hotspot", "chimeric",
}
STRICT_NOISE = {
    "exonic_antisense",
    "intergenic_sparse", "intergenic_repeat", "intergenic_hotspot", "chimeric",
}


def parse_args():
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--bam", required=True)
    p.add_argument("--truth", required=True, help="ground_truth.tsv from synthesize_bam.py")
    p.add_argument("--gtf", required=True)
    p.add_argument("--fasta", required=True)
    p.add_argument("--outdir", required=True)
    p.add_argument("--sample-name", default="synthetic")
    p.add_argument("--repeats", default=None,
                   help="RepeatMasker BED passed through to scnoisemeter. "
                        "synthesize_bam.py emits outdir/repeats.bed; pass that here "
                        "to enable INTERGENIC_REPEAT classification.")
    p.add_argument("--skip-run", action="store_true",
                   help="Skip the scnoisemeter invocation (use existing outputs in --outdir)")
    return p.parse_args()


def run_scnoisemeter(args):
    cmd = [
        "python3", "-m", "scnoisemeter.cli", "run",
        "--bam", args.bam,
        "--gtf", args.gtf,
        "--reference", args.fasta,
        "--platform", "ont",
        "--whitelist-db", "none",
        "--output-dir", args.outdir,
        "--sample-name", args.sample_name,
    ]
    if args.repeats:
        cmd.extend(["--repeats", args.repeats])
    print("running:", " ".join(cmd), file=sys.stderr)
    subprocess.run(cmd, check=True)


def load_cell_metrics(outdir: Path, sample: str) -> pd.DataFrame:
    path = outdir / f"{sample}.cell_metrics.tsv"
    df = pd.read_csv(path, sep="\t")
    return df


def load_ground_truth(path: Path) -> pd.DataFrame:
    return pd.read_csv(path, sep="\t")


def build_confusion_matrix(cell_metrics: pd.DataFrame, truth: pd.DataFrame) -> pd.DataFrame:
    """Return a (true_category x assigned_category) count matrix for Exp 1 CBs.

    Multiple CBs may share one true_category (HOTSPOT / NOVEL / REPEAT use
    three CBs each to satisfy the >= 3 barcode threshold of the intergenic
    profiler); this function sums across those CBs per true_category.
    """
    exp1 = truth[truth["notes"] == "exp1"].copy()
    cb_to_true = dict(zip(exp1["cell_barcode"], exp1["true_category"]))
    counts: dict[str, dict[str, float]] = defaultdict(lambda: defaultdict(float))
    for _, row in cell_metrics.iterrows():
        cb = row["cell_barcode"]
        if cb not in cb_to_true:
            continue
        true_cat = cb_to_true[cb].lower()
        n = row["n_reads"]
        for col in CATEGORY_COLUMNS:
            frac = row.get(f"read_frac_{col}", 0) or 0
            if pd.isna(frac):
                frac = 0
            counts[true_cat][col] += round(frac * n)
    mat = pd.DataFrame(counts).T.fillna(0).astype(int)
    mat = mat.reindex(columns=CATEGORY_COLUMNS, fill_value=0)
    mat.index.name = "true_category"
    mat = mat.sort_index()
    return mat


def per_category_metrics(mat: pd.DataFrame) -> pd.DataFrame:
    """Per-category accuracy (diagonal / row sum)."""
    rows = []
    for true_cat in mat.index:
        row_sum = mat.loc[true_cat].sum()
        correct = mat.loc[true_cat].get(true_cat, 0)
        acc = correct / row_sum if row_sum else float("nan")
        rows.append({
            "true_category": true_cat,
            "total_reads": int(row_sum),
            "correct_reads": int(correct),
            "accuracy": round(acc, 4),
        })
    return pd.DataFrame(rows)


def noise_dose_response(cell_metrics: pd.DataFrame, truth: pd.DataFrame) -> pd.DataFrame:
    """Build per-cell table: true_noise_fraction vs reported noise fractions."""
    exp2 = truth[truth["notes"].str.startswith("exp2", na=False)]
    if exp2.empty:
        return pd.DataFrame()
    cb_to_true_frac: dict[str, float] = {}
    for cb, sub in exp2.groupby("cell_barcode"):
        note = sub["notes"].iloc[0]
        # notes format: exp2;true_noise=X.XX;...
        parts = dict(p.split("=", 1) for p in note.split(";") if "=" in p)
        cb_to_true_frac[cb] = float(parts.get("true_noise", 0))

    rows = []
    for _, row in cell_metrics.iterrows():
        cb = row["cell_barcode"]
        if cb not in cb_to_true_frac:
            continue
        true_frac = cb_to_true_frac[cb]
        cons_frac = sum((row.get(f"read_frac_{c}", 0) or 0) for c in CONSERVATIVE_NOISE)
        strict_frac = sum((row.get(f"read_frac_{c}", 0) or 0) for c in STRICT_NOISE)
        reported = row.get("noise_read_frac", float("nan"))
        rows.append({
            "cell_barcode": cb,
            "n_reads": int(row["n_reads"]),
            "true_noise_frac": round(true_frac, 4),
            "reported_noise_frac": round(float(reported), 4),
            "recomputed_conservative": round(cons_frac, 4),
            "recomputed_strict": round(strict_frac, 4),
            "delta_vs_true": round(float(reported) - true_frac, 4),
        })
    df = pd.DataFrame(rows).sort_values("true_noise_frac").reset_index(drop=True)
    return df


def main():
    args = parse_args()
    outdir = Path(args.outdir)

    if not args.skip_run:
        run_scnoisemeter(args)

    cm = load_cell_metrics(outdir, args.sample_name)
    gt = load_ground_truth(Path(args.truth))

    print("Building confusion matrix (Exp 1)...", file=sys.stderr)
    confusion = build_confusion_matrix(cm, gt)
    confusion_path = outdir / "benchmark_confusion_matrix.tsv"
    confusion.to_csv(confusion_path, sep="\t")
    print(f"  -> {confusion_path}", file=sys.stderr)

    metrics = per_category_metrics(confusion)
    metrics_path = outdir / "benchmark_per_category_metrics.tsv"
    metrics.to_csv(metrics_path, sep="\t", index=False)
    print(f"  -> {metrics_path}", file=sys.stderr)

    print("Building noise dose-response (Exp 2)...", file=sys.stderr)
    dose = noise_dose_response(cm, gt)
    dose_path = outdir / "benchmark_noise_dose_response.tsv"
    dose.to_csv(dose_path, sep="\t", index=False)
    print(f"  -> {dose_path}", file=sys.stderr)

    summary = {
        "exp1_categories": len(metrics),
        "exp1_mean_accuracy": float(metrics["accuracy"].mean()) if len(metrics) else None,
        "exp1_min_accuracy": float(metrics["accuracy"].min()) if len(metrics) else None,
        "exp2_cells": int(len(dose)),
        "exp2_mean_abs_delta": float(dose["delta_vs_true"].abs().mean()) if len(dose) else None,
        "exp2_max_abs_delta": float(dose["delta_vs_true"].abs().max()) if len(dose) else None,
    }
    summary_path = outdir / "benchmark_summary.json"
    with open(summary_path, "w") as fh:
        json.dump(summary, fh, indent=2)
    print(f"  -> {summary_path}", file=sys.stderr)

    print("\n=== Exp 1: per-category accuracy ===", file=sys.stderr)
    print(metrics.to_string(index=False), file=sys.stderr)

    print("\n=== Exp 2: noise dose-response ===", file=sys.stderr)
    print(dose.to_string(index=False), file=sys.stderr)

    print("\n=== Summary ===", file=sys.stderr)
    print(json.dumps(summary, indent=2), file=sys.stderr)


if __name__ == "__main__":
    main()
